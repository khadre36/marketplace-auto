      function getPageList(totalPages, page, maxLength) {
        function range(start, end) {
          return Array.from(Array(end - start + 1), (_, i) => i + start);
        }

        var sideWidth = maxLength < 9 ? 1 : 2;
        var lefWidth = (maxLength - sideWidth * 2 - 3) >> 1;
        var rightWidth = (maxLength - sideWidth * 2 - 3) >> 1;

        if (totalPages <= maxLength) {
          return range(1, totalPages);
        }
        if (page <= maxLength - sideWidth - 1 - rightWidth) {
          return range(1, maxLength - sideWidth - 1).concat(0, range(totalPages - sideWidth + 1, totalPages));
        }
        if (page >= totalPages - sideWidth - 1 - rightWidth) {
          return range(1, sideWidth).concat(0, range(totalPages - sideWidth - 1 - rightWidth - lefWidth, totalPages));
        }

        return range(1, sideWidth).concat(0, range(page - lefWidth, page + rightWidth), 0, range(totalPages - sideWidth + 1, totalPages));
      }

      $(function () {
        var numberOfItems = $(".card-content .card").length;
        var limiterPerPage = 12;
        var totalPages = Math.ceil(numberOfItems / limiterPerPage);
        var paginatioSize = 7;
        var currentPage;

        function showPage(whichPage) {
          if (whichPage < 1 || whichPage > totalPages) return false;
          currentPage = whichPage;

          $(".card-content .card").hide().slice((currentPage - 1) * limiterPerPage, currentPage * limiterPerPage).show();
          $(".paginatio li").slice(1, -1).remove();
          getPageList(totalPages, currentPage, paginatioSize).forEach(item => {
            $("<li>").addClass("page-ite").addClass(item ? "current-page" : "dots")
              .toggleClass("active", item === currentPage).append($("<a>").addClass("page-lin")
                .attr({ href: "javascript:void(0)" }).text(item || "...")).insertBefore(".next");
          });
          $(".previous").toggleClass("disabled", currentPage === 1);
          $(".next").toggleClass("disabled", currentPage === totalPages);
          return true;
        }

        $(".paginatio").append(
          $("<li>").addClass("page-ite").addClass("previous").append($("<a>").addClass("page-lin").attr({ href: "javascript:void(0)" }).text("Prev")),
